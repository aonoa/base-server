create table menus
(
    id                    bigint generated by default as identity
        primary key,
    create_time           timestamp with time zone not null,
    update_time           timestamp with time zone not null,
    pid                   bigint                   not null,
    type                  smallint                 not null,
    status                boolean                  not null,
    name                  varchar                  not null,
    title                 varchar                  not null,
    icon                  varchar                  not null,
    "order"               integer                  not null,
    path                  varchar                  not null,
    component             varchar                  not null,
    redirect              varchar                  not null,
    link                  varchar                  not null,
    iframe_src            varchar                  not null,
    active_icon           varchar                  not null,
    active_path           varchar                  not null,
    max_num_of_open_tab   smallint                 not null,
    ignore_auth           boolean                  not null,
    keepalive             boolean                  not null,
    permission            varchar                  not null,
    affix_tab             boolean                  not null,
    affix_tab_order       bigint                   not null,
    hide_in_menu          boolean default false    not null,
    hide_in_tab           boolean default false    not null,
    hide_in_breadcrumb    boolean default false    not null,
    hide_children_in_menu boolean default false    not null
);

